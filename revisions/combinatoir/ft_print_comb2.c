void	ft_print_comb2(void)
{
	int a;
	int b;

	a = -1;
	while (++a < 100)
	{
		b = a;
		while (++b < 100)
		{
			ft_putchar('0' + a / 10);
			ft_putchar('0' + a % 10);
			ft_putchar(' ');
			ft_putchar('0' + b / 10);
			ft_putchar('0' + b % 10);
			if (a + b < 197)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
	}
}
