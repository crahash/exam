void	ft_putnbr(int nb)
{
	int		i;
	char	a[11];

	i = 0;
	if (nb < 0)
	{
		ft_putchar('-');
	}
	while (1)
	{
		a[i++] = '0' + ((nb > 0) * 2 - 1) * (nb % 10);
		if (!(nb /= 10))
		{
			break ;
		}
	}
	while (i--)
	{
		ft_putchar(a[i]);
	}
}
